    $(document).on('change', 'input:checkbox', function() { 
        if (this.checked) {
            var obs = $(this).text();
            if(!obs.includes($(this).text())){
                obs += $(this).text()+"\n";
            }
            else {
                if(obs.includes($(this).text())){
                    obs = obs.replace($(this).text(),"");
                }
            }
            $('#observacoes').text() = obs;
        }
       console.log($(this).text())
    });
